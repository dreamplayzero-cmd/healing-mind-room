function mask_pii(text) {
  let res = text;
  res = res.replace(/\d{2,3}[-\s]?\d{3,4}[-\s]?\d{4}/g, "[연락처]");
  res = res.replace(/[\w.+-]+@[\w-]+\.[\w.-]+/g, "[이메일]");
  res = res.replace(/\d{6}[-\s]?[1-4]\d{6}/g, "[주민번호]");
  return res;
}

const INJECTION_PATTERNS = ["이전 지시", "지시를 무시", "시스템 프롬프트", "ignore previous", "system prompt", "너의 규칙", "역할을 잊"];
function looks_like_injection(text) {
  const low = text.toLowerCase();
  return INJECTION_PATTERNS.some(p => low.includes(p.toLowerCase()));
}

