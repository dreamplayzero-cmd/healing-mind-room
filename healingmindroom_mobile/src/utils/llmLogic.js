async function call_llm(system_prompt, user_message) {
  const model = genAI.getGenerativeModel({
    model: MODEL,
    systemInstruction: system_prompt,
  });
  const result = await model.generateContent(user_message);
  return result.response.text();
}

export function parseResponseIntoSteps(text) {
  // Parses the 5-step response string into an object
  const steps = {
    step1: "",
    step2: "",
    step3: "",
    step4: "",
    step5: ""
  };
  
  const blocks = text.split(/(?=\d\.\s)/);
  for (const block of blocks) {
    const content = block.replace(/^\d\.\s.*\n?/, '').trim();
    if (block.startsWith("1.")) steps.step1 = content;
    else if (block.startsWith("2.")) steps.step2 = content;
    else if (block.startsWith("3.")) steps.step3 = content;
    else if (block.startsWith("4.")) steps.step4 = content;
    else if (block.startsWith("5.")) steps.step5 = content;
    else if (!steps.step1 && content) steps.step1 = content; // Fallback
  }
  return steps;
}

export async function get_response_by_mode(use_llm, gen_key, concern_type, concern_text) {
  if (!concern_text || concern_text.trim().length < 5) {
      return "고민을 조금 더 적어주시면(5자 이상) 더 잘 도와드릴 수 있어요.";
  }

  const safe_text = mask_pii(concern_text.trim());

  if (looks_like_crisis(safe_text)) {
      return CRISIS_MESSAGE;
  }

  if (looks_like_injection(safe_text)) {
      return "고민 내용에 집중해 주세요.";
  }

  const system_prompt = build_system_prompt(gen_key);
  const user_message = `[고민 유형] ${concern_type}\n[고민 내용] ${safe_text}`;

  if (!use_llm) {
      // JSON fallback
      return fallbackToJson(gen_key, concern_type);
  } else {
      try {
          const answer = await call_llm(system_prompt, user_message);
          return answer + DISCLAIMER;
      } catch (e) {
          console.error("LLM Error, falling back to JSON:", e);
          return fallbackToJson(gen_key, concern_type);
      }
  }
}

function fallbackToJson(gen_key, concern_type) {
  const gen_responses = JSON_RESPONSES[gen_key] || {};
  let matched = null;
  for (const key of Object.keys(gen_responses)) {
      const keywords = key.split('·');
      if (keywords.some(k => concern_type.includes(k))) {
          matched = gen_responses[key];
          break;
      }
  }
  if (!matched) {
      matched = gen_responses["기타"] || {};
  }

  const responseText = `1. 감정 공감
${matched["감정공감"] || ""}

2. 상황 분석
${matched["상황분석"] || ""}

3. 관점 전환
${matched["관점전환"] || ""}

4. 행동 제안
${matched["행동제안"] || ""}

5. 희망 메시지
${matched["희망메시지"] || ""}`;

  return responseText + DISCLAIMER;
}
