# 4. 가드레일 (16일 로드맵 DAY 10 '필수' 항목 — 상담 서비스라 특히 중요)
# ──────────────────────────────────────────────────────────────────────────
# 4-1) PII 마스킹: 전화번호·이메일·주민번호 패턴을 가려서 LLM·로그에 원본이 남지 않게
def mask_pii(text: str) -> str:
    text = re.sub(r"\d{2,3}[-\s]?\d{3,4}[-\s]?\d{4}", "[연락처]", text)          # 전화번호
    text = re.sub(r"[\w.+-]+@[\w-]+\.[\w.-]+", "[이메일]", text)                  # 이메일
    text = re.sub(r"\d{6}[-\s]?[1-4]\d{6}", "[주민번호]", text)                   # 주민번호
    return text

# 4-2) 프롬프트 인젝션 간단 탐지: AI를 조종하려는 입력을 거름
INJECTION_PATTERNS = ["이전 지시", "지시를 무시", "시스템 프롬프트", "ignore previous",
                      "system prompt", "너의 규칙", "역할을 잊"]
def looks_like_injection(text: str) -> bool:
    low = text.lower()
    return any(p.lower() in low for p in INJECTION_PATTERNS)

# 4-3) 위기 신호 감지: 자해·극단적 위기 표현이면 상담 대신 전문기관으로 안내 (안전 우선)
CRISIS_PATTERNS = ["죽고 싶", "자해", "극단적 선택", "살기 싫", "사라지고 싶"]
CRISIS_MESSAGE = (
    "지금 많이 힘드신 것 같아 마음이 쓰입니다. 이 부분만큼은 혼자 견디지 않으셨으면 합니다.\n\n"
    "지금 바로 전문가와 이야기 나눌 수 있는 곳이 있어요.\n"
    "• 자살예방 상담전화 ☎ 109 (24시간)\n"
    "• 정신건강 상담전화 ☎ 1577-0199\n\n"
    "당신은 혼자가 아닙니다. 잠시라도 위 번호로 연락해 마음을 나눠 주세요."
)
def looks_like_crisis(text: str) -> bool:
    return any(p in text for p in CRISIS_PATTERNS)

# ──────────────────────────────────────────────────────────────────────────
# 5. LLM 호출
# ──────────────────────────────────────────────────────────────────────────
def call_llm(system_prompt: str, user_message: str) -> str:
    model = genai.GenerativeModel(MODEL, system_instruction=system_prompt)
    response = model.generate_content(user_message)
    return response.text

# ──────────────────────────────────────────────────────────────────────────
# 6. 메인 상담 함수
# ──────────────────────────────────────────────────────────────────────────
def get_response_by_mode(use_llm, gen_key, concern_type, concern_text, system_prompt, user_message):
    """
    use_llm=False → JSON 모드 (즉시 응답, 안정적)
    use_llm=True  → LLM 모드 (실시간 AI 응답, 5초 소요)
    """
    if not use_llm:
        # JSON 모드: 사전 작성된 응답 사용
        gen_responses = JSON_RESPONSES.get(gen_key, {})
        
        # 고민 유형 매칭 (부분 일치)
        matched = None
        for key in gen_responses:
            if any(word in concern_type for word in key.split("·")):
                matched = gen_responses[key]
                break
        
        if not matched:
            matched = gen_responses.get("기타", {})
        
        response = f"""1. 감정 공감
{matched.get('감정공감', '')}

2. 상황 분석
{matched.get('상황분석', '')}

3. 관점 전환
{matched.get('관점전환', '')}

4. 행동 제안
{matched.get('행동제안', '')}

5. 희망 메시지
{matched.get('희망메시지', '')}"""
        
        return response + DISCLAIMER
    
    else:
        # LLM 모드: 실시간 AI 응답
        try:
            answer = call_llm(system_prompt, user_message)
            return answer + DISCLAIMER
        except Exception as e:
            # LLM 실패 시 자동으로 JSON 폴백
            print(f"LLM 오류, JSON 폴백: {e}")
            return get_response_by_mode(False, gen_key, concern_type, concern_text, system_prompt, user_message)

# ──────────────────────────────────────────────────────────────────────────
# 6. 메인 상담 함수
# ──────────────────────────────────────────────────────────────────────────
def counsel(gen_key, concern_type, concern_text, use_llm):
    if not concern_text or len(concern_text.strip()) < 5:
        return "고민을 조금 더 적어주시면(5자 이상) 더 잘 도와드릴 수 있어요.", None

    safe_text = mask_pii(concern_text.strip())
    
    if looks_like_crisis(safe_text):
        return CRISIS_MESSAGE, None
    
    if looks_like_injection(safe_text):
        return "고민 내용에 집중해 주세요.", None

    system_prompt = build_system_prompt(gen_key)
    user_message = f"[고민 유형] {concern_type}\n[고민 내용] {safe_text}"
    
    # 모드에 따라 응답 방식 선택
    answer = get_response_by_mode(use_llm, gen_key, concern_type, concern_text, system_prompt, user_message)
    
    state = {"gen": gen_key, "concern": safe_text, "answer": answer}
    return answer, state

# ──────────────────────────────────────────────────────────────────────────
# 7. 관점 전환 — 같은 고민을 상대방(가족 등) 시각으로 다시 풀어줌
# ──────────────────────────────────────────────────────────────────────────
def shift_perspective(state, perspective):
    if not state:
        return "먼저 상담을 받아주세요. 그다음 관점 전환을 사용할 수 있어요."
    gen_key = state["gen"]
    system_prompt = build_system_prompt(gen_key) + (
        f"\n\n[지금 할 일] 사용자의 같은 고민을 '{perspective}'의 관점에서 따뜻하게 재해석해 주세요. "
        "상대방이 속으로 어떻게 느끼고 있을지, 왜 그렇게 행동하는지를 3~5문장으로 설명하고, "
        "마지막에 관계 회복을 위한 한 문장을 덧붙이세요."
    )
    user_message = f"[원래 고민] {state['concern']}\n[관점] {perspective}"
    try:
        return f"💡 {perspective}\n\n" + call_llm(system_prompt, user_message)
    except Exception as e:
        return f"(일시적 오류입니다. 다시 시도해 주세요.)\n\n[디버그] {e}"

